// Variables para manejar el estado
let indiceActual = 0 // Índice de la frase actual
let indiceAnterior = null; // Índice de la frase anterior

// Función para mostrar una cita en el contenedor
function mostrarCita(indice) {
    // Asegúrate de que el índice esté dentro del rango
    if (indice < 0 || indice >= Values.length) {
        console.error('Índice fuera de rango.');
        return;
    }

    // Obtiene el objeto en el índice
    const citaAleatoria = Values[indice];

    // Selecciona el contenedor en el HTML
    const contenedor = document.getElementById('frases');

    // Limpia el contenido anterior
    contenedor.innerHTML = '';

    // Crea un nuevo elemento <p> para la cita
    const parrafo = document.createElement('p');
    parrafo.innerHTML = `"${citaAleatoria.cita}"<br>- ${citaAleatoria.autor}`;

    // Añade el nuevo elemento <p> al contenedor
    contenedor.appendChild(parrafo);
}

// Función para generar una nueva frase aleatoria
function nuevaFrase() {
    // Genera un nuevo índice aleatorio
    let nuevoIndice;
    do {
        nuevoIndice = Math.floor(Math.random() * Values.length);
    } while (nuevoIndice === indiceActual); // Asegura que sea diferente de la actual

    // Guarda el índice actual como anterior
    indiceAnterior = indiceActual;
    // Actualiza el índice actual
    indiceActual = nuevoIndice;

    // Muestra la nueva cita
    mostrarCita(indiceActual);
}

// Función para mostrar la frase anterior
function fraseAnterior() {
    if (indiceAnterior === null) {
        console.warn('No hay frase anterior.');
        return;
    }

    // Guarda el índice actual como anterior
    const indiceTemporal = indiceActual;
    indiceActual = indiceAnterior;
    indiceAnterior = indiceTemporal;

    // Muestra la frase anterior
    mostrarCita(indiceActual);
}

// Configura los manejadores de eventos para los botones
document.getElementById('nuevaFrase').addEventListener('click', nuevaFrase);
document.getElementById('anteriorFrase').addEventListener('click', fraseAnterior);

// Inicializa con una frase aleatoria al cargar la página
nuevaFrase();